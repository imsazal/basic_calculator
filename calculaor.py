
num1 = float(input("enter a first number: "))
op = input("enter operator: ")
num2 = float(input("enter a second number: "))

if op == "+":
    print(num1 + num2)
elif op == "-":
    print(num1 - num2)
elif op == "/":
    print(num1 / num2)
elif op == "*":
    print(num1 * num2)
else: ("invalid operator")

if op == "//":
    print(num1 // num2)

if op == "%":
    print(num1/100 * 100)








